                'ip' => 'required',
                'emp_id' => 'required|unique:users',
                'division' => 'required'
            ]
        );

        if (User::where('division', 1)->count() > 10) {
            return 'your division has enough members';
        } else {
            $users = User::updateOrcreate(['id' => $request->get('id')], [
